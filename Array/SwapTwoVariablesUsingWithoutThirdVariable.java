package Array;

public class SwapTwoVariablesUsingWithoutThirdVariable {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;

        // Swapping
        a = a+b;
        b = a-b;
        a = a-b;
        System.out.println(a);
        System.out.println(b);
    }
}







